			//$bSetupFilter = TRUE; // No need to set up, just use default
		} else {
			$bRestoreSession = !$this->SearchCommand;

			// Field producttypename
			if ($this->GetDropDownValue($this->producttypename)) {
				$bSetupFilter = TRUE;
			} elseif ($this->producttypename->DropDownValue <> EWR_INIT_VALUE && !isset($_SESSION['sv_product_type_producttypename'])) {
				$bSetupFilter = TRUE;
			}
			if (!$this->ValidateForm()) {
				$this->setFailureMessage($grFormError);
