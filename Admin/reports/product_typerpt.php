	// Setup list of filters
	function SetupFilterList($filter) {
		if (!is_array($filter))
			return FALSE;

		// Field producttypename
		$bRestoreFilter = FALSE;
		if (array_key_exists("sv_producttypename", $filter)) {
			$sWrk = $filter["sv_producttypename"];
			if (strpos($sWrk, "||") !== FALSE)
				$sWrk = explode("||", $sWrk);
			$this->SetSessionDropDownValue($sWrk, @$filter["so_producttypename"], "producttypename");
			$bRestoreFilter = TRUE;
		}
		if (array_key_exists("sel_producttypename", $filter)) {
			$sWrk = $filter["sel_producttypename"];
			$sWrk = explode("||", $sWrk);
			$this->producttypename->SelectionList = $sWrk;
			$_SESSION["sel_product_type_producttypename"] = $sWrk;
			$this->SetSessionDropDownValue(EWR_INIT_VALUE, "", "producttypename"); // Clear drop down
			$bRestoreFilter = TRUE;
		}
		if (!$bRestoreFilter) { // Clear filter
			$this->SetSessionDropDownValue(EWR_INIT_VALUE, "", "producttypename");
			$this->producttypename->SelectionList = "";
			$_SESSION["sel_product_type_producttypename"] = "";
		}
		return TRUE;
	}

