		$this->BuildDropDownFilter($this->producttypename, $sFilter, $this->producttypename->SearchOperator, FALSE, TRUE); // Field producttypename

		// Save parms to session
		$this->SetSessionDropDownValue($this->producttypename->DropDownValue, $this->producttypename->SearchOperator, 'producttypename'); // Field producttypename

